import type { Metadata } from "next";
import { Geist } from "next/font/google";
import { PropsWithChildren } from "react";

import "./globals.css";
import Header from "root/components/static/Header/Header";
import Footer from "root/components/static/Footer/Footer";
import { Affix } from "@kadoui/react";
import { ArrowUpIcon } from "lucide-react";

const geist = Geist({
  subsets: ["latin"],
  variable: "--font-geist",
  display: "swap",
});

const themeScript = `
  (function() {
    try {
      const themeHandler = (ev) => {
        const theme = ev.matches ? "dark" : "light";
        document.querySelector("html").setAttribute("data-theme", theme);
      };

      const darkModeQuery = window.matchMedia("(prefers-color-scheme: dark)");

      darkModeQuery.addEventListener("change", themeHandler)

      themeHandler(darkModeQuery)
    } catch (err) {
      console.log(err) 
    }
  })();
`;

export const metadata: Metadata = {
  title: "Create Next App",
  description: "Generated by create next app",
};

const RootLayout = ({ children }: PropsWithChildren) => {
  return (
    <html lang="en" className={`${geist.variable}`} suppressHydrationWarning>
      <head>
        <script dangerouslySetInnerHTML={{ __html: themeScript }} />
      </head>
      <body>
        <Header />
        {children}
        <Footer />

        <Affix className="ltr:affix-br rtl:affix-bl btn-fill btn-square group not-[.affix-active]:scale-90">
          <ArrowUpIcon className="btn-icon-size transition-transform duration-300 group-not-[.affix-active]:translate-y-full" />
        </Affix>
      </body>
    </html>
  );
};

export default RootLayout;
